# ── Request Logging Middleware ────────────────────────────────────────────────

@app.middleware("http")
async def log_requests(request: Request, call_next):
    """
    Log every incoming request and outgoing response with timing.

    Logs method, path, status code, and duration_ms for every request.
    On unhandled exception, logs the crash details and re-raises so
    FastAPI's exception handlers still fire correctly.

    Parameters
    ----------
    request : Request
    call_next : Callable
        Next middleware or route handler in the chain.
    """
    start_time = time.time()

    logger.info(
        "[REQUEST] %s %s — client=%s",
        request.method,
        request.url.path,
        request.client.host if request.client else "unknown",
    )

    try:
        response = await call_next(request)
        duration_ms = round((time.time() - start_time) * 1000, 2)

        logger.info(
            "[RESPONSE] %s %s → %d (%sms)",
            request.method,
            request.url.path,
            response.status_code,
            duration_ms,
        )

        return response

    except Exception as e:
        duration_ms = round((time.time() - start_time) * 1000, 2)

        logger.exception(
            "[CRASH] %s %s failed after %sms — error=%s",
            request.method,
            request.url.path,
            duration_ms,
            str(e),
        )
        raise


# ── Startup ───────────────────────────────────────────────────────────────────

@app.on_event("startup")
async def on_startup() -> None:
    """
    Application startup handler — runs once when uvicorn starts.

    Steps
    -----
    1. Create all SQLAlchemy ORM tables if they do not exist.
       Uses create_all() — safe to call on existing databases.
    2. Log environment configuration for verification.

    Failure Behaviour
    -----------------
    Any exception here raises RuntimeError which causes uvicorn to
    exit immediately with a clear error message. This is intentional —
    a server that cannot connect to its database should not start.

    Raises
    ------
    RuntimeError
        If database initialization fails.
    """
    logger.info(
        "[startup] Starting Advanced GitHub Code Reviewer P4 — "
        "version=4.0.0"
    )

    try:
        logger.info("[startup] Initializing database tables")

        async with engine.begin() as conn:
            await conn.run_sync(Base.metadata.create_all)

        logger.info("[startup] Database tables ready")

        logger.info(
            "[startup] Configuration — "
            "environment=%s db=%s langsmith_tracing=%s",
            settings.ENVIRONMENT,
            bool(settings.DATABASE_URL),
            settings.LANGSMITH_TRACING,
        )

        logger.info("[startup] Application startup complete")

    except Exception as e:
        logger.exception("[startup] Database initialization failed — %s", str(e))
        raise RuntimeError(
            f"Application startup failed: {e}"
        ) from e


# ── Global Exception Handlers ─────────────────────────────────────────────────

@app.exception_handler(CustomException)
async def custom_exception_handler(
    request: Request,
    exc: CustomException,
) -> JSONResponse:
    """
    Convert unhandled CustomException to HTTP 500 JSON response.

    CustomExceptions that escape route handlers and service functions
    are caught here as a last resort. Routes should catch CustomException
    and convert to HTTPException with appropriate status codes before
    this handler fires.

    Parameters
    ----------
    request : Request
    exc : CustomException

    Returns
    -------
    JSONResponse
        HTTP 500 with exception message in detail field.
    """
    logger.error(
        "[exception_handler] CustomException on %s — %s",
        request.url.path,
        str(exc),
    )
    return JSONResponse(
        status_code=500,
        content={"detail": str(exc)},
    )


@app.exception_handler(Exception)
async def global_exception_handler(
    request: Request,
    exc: Exception,
) -> JSONResponse:
    """
    Catch-all handler for any unhandled exception.

    Fires for exceptions that are not HTTPException and not CustomException.
    Logs the full traceback at DEBUG level for development and returns
    a generic error message to the client.

    Parameters
    ----------
    request : Request
    exc : Exception

    Returns
    -------
    JSONResponse
        HTTP 500 with generic error message.
    """
    logger.error(
        "[exception_handler] Unhandled exception on %s — type=%s message=%s",
        request.url.path,
        type(exc).__name__,
        str(exc),
    )
    logger.debug("[exception_handler] Traceback:\n%s", traceback.format_exc())

    return JSONResponse(
        status_code=500,
        content={"detail": "Internal Server Error"},
    )


