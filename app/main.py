# ── Dashboard Endpoint ────────────────────────────────────────────────────────
# Defined here (not in review.py) to avoid route ordering conflict.
# review.py defines /reviews/{param} paths which would shadow /reviews/
# if defined there. Registering after include_router() ensures /reviews/
# is matched before parameterized paths.

@app.get(
    "/reviews/",
    tags=["reviews"],
    summary="List all reviews (dashboard)",
    description=(
        "Returns all reviews across all repositories, newest first. "
        "Used by the Streamlit admin dashboard to populate the PR list."
    ),
)
async def get_all_reviews(
    db: AsyncSession = Depends(get_db),
):
    """
    List all reviews across all repositories.

    Returns enriched dicts with PR number, repo name, PR title, author,
    branch, and thread_id for the Streamlit dashboard. Newest first.
    """
    logger.info("[get_all_reviews] Dashboard request — fetching all reviews")

    try:
        reviews = await list_all_reviews(db)
        logger.info(
            "[get_all_reviews] Returning %d review(s)", len(reviews),
        )
        return reviews

    except CustomException as e:
        logger.error("[get_all_reviews] Service error — %s", str(e))
        raise HTTPException(status_code=500, detail=str(e))

    except Exception as e:
        logger.exception("[get_all_reviews] Unexpected error")
        raise HTTPException(status_code=500, detail="Internal server error")


